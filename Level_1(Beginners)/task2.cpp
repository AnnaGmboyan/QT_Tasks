#include <iostream>

int main() 
{
    bool bvar = true;
    char cvar = 'A';
    int ivar = 5;
    float fvar = 6.022;
    double dvar = 10.6;

	std::cout << "Bool Variable: " << bvar << std::endl;
	std::cout << "Char Variable: " << cvar << std::endl;
	std::cout << "Integer Variable: " << ivar << std::endl;
	std::cout << "Float Variable: " << fvar << std::endl;
	std::cout << "Double Variable: " << dvar << std::endl;

    return 0;
}

